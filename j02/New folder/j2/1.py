number1 = float(input("Enter number1: "))
number2 = float(input("Enter number2: "))

sums = number1 + number2
sub = number1 - number2
mul = number1 * number2
div = number1 / number2

print(f"{number1} + {number2} = {sums}")
print(f"{number1} - {number2} = {sub}")
print(f"{number1} * {number2} = {mul}")
print(f"{number1} / {number2} = {div}")